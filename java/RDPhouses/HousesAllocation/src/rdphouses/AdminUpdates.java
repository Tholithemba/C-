    @Override
    public String setValues(){
        
        String selected_item = (String)fieldToUpdateSelected.getSelectedItem();
        
        req_field.setRFUsername(username_txt.getText());
        
        switch(selected_item){
            
            case "Firstname": req_field.setFieldName("admin_firstname");
                break;
            case "Lastname": req_field.setFieldName("admin_lastname");
                break;
            case "Email": req_field.setFieldName("admin_email");
                break;
            case "Username": req_field.setFieldName("admin_username");
                break;
            case "Password": req_field.setFieldName("admin_password");
                break;
                
            default:JOptionPane.showMessageDialog(null, "selection problem contact administrator");
                    System.exit(0);
                   break;
        }
        
        String query_statement = 
                "select "+req_field.getFieldName()+" from ADMINISTRATOR"
                + " where admin_username=?";
        
        return query_statement;
    }
    
    
    @Override
    public boolean validateInput(){
        boolean input_valid = true;
        
        if(fieldToUpdateSelected.getSelectedItem().equals("Select"))
            return outputStatement("select field you want to update");
        
        if(username_txt.getText().equals(""))
            return outputStatement("Enter username");
        
        return input_valid;
    }
    
    private boolean passwordEnryption()
    {
        GenerateRandomChar grc = new GenerateRandomChar();
        String updated_password = val_tobe_updated.getText();
        if(updated_password.length() != 10)
            return outputStatement("password length must be of 10 charectors");
        else
        {
            String encrypt = grc.passwordEncryption(updated_password);
            req_field.setFieldData(encrypt);
            return true;
        }

    }
    
    @Override
    public boolean outputStatement(String message){
        
        warning_txt.setText(message);
        clearCells();
        return false;
    }
    
    @Override
    public void clearCells(){
        
        username_txt.setText("");
        val_tobe_updated.setText("");
    }


    @Override
    public void warningTextInitialised(){
        warning_txt.setText("");
    }

    @Override
    public void selectedField(){
        
        String query = setValues();
        req_field.getDataTobeUpdated(query);
        String fied_data = req_field.getFieldData();
        
        if(fied_data != null)
        {
            val_tobe_updated.setText(fied_data);
        }else{
            warning_txt.setText("username does not exist");
            clearCells();
        }
    }
    
    @Override
    public boolean modifiedTextFieldNotEmpty(){
        if(val_tobe_updated.getText().equals(""))
            return outputStatement("text field to be modified is empty");
        
        return true;
    }
    
    @Override
    public String queryStatement(){
        
        String query = 
        "update ADMINISTRATOR set "+req_field.getFieldName()+"=? where "+
                "admin_username=?";
        
        return query;
    }

